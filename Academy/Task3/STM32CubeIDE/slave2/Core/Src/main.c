typedef struct {
		uint16_t case_id;
		float voltage;
		uint16_t wheel_speed;
	}Datasets;

void pack_data(uint8_t * rxbuffer, uint8_t * txbuffer){
	for (int j=0;j<5;j++) txbuffer[j] = rxbuffer[j];
	  Datasets data_array[] = {
	      {0x1001, 12.3, 200},
	      {0x1002, 14.2, 150},
	      {0x1003, 13.8, 100},
	      {0x1004, 11.5, 220},
	      {0x1005, 10.9, 280},
		  {0x1000, 0.0, 0}
	  };
	  uint16_t case_id = ((uint16_t)rxbuffer[0] << 8) | rxbuffer[1];
	  int i;
	  for (i=0; i<6;i++){
		  if (data_array[i].case_id == case_id)break;
	  }
	  uint16_t descaled_voltage = data_array[i].voltage * 4095 / 24.0;
	  uint16_t descaled_wheel_speed = data_array[i].wheel_speed * 4095 / 300;

	  txbuffer[5] = (uint8_t)(descaled_voltage >> 4);
	  txbuffer[6] = (uint8_t)((descaled_voltage & 0x0F) << 4) | (uint8_t)(descaled_wheel_speed >> 8);
	  txbuffer[7] = (uint8_t)(descaled_wheel_speed & 0xFF);

}
